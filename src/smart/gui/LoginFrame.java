    private boolean validateInput() {
        userid=txtUserId.getText().trim();
        char[] pws=jPassword.getPassword();
        password=String.valueOf(pws).trim();
        if(userid.isEmpty() ||  password.isEmpty())
            return false;
        return true;
    }
    
    private String getUserType()
    {
        if(jrManager.isSelected())
            return jrManager.getText();
        else if(jrReceptionist.isSelected())
            return jrReceptionist.getText();
        else
            return null;
    }
    
