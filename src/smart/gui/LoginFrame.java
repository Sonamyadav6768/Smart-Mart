    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if(validateInput()==false)
        {
            JOptionPane.showMessageDialog(null,"Please fill all the details ","ERROR",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String usertype=getUserType();
        if(usertype==null)
        {
            JOptionPane.showMessageDialog(null,"Please select the user type ","ERROR",JOptionPane.ERROR_MESSAGE);
            return ;
        }
        UserPojo u=new UserPojo();
        u.setUserId(userid);
        u.setPassword(password);
        u.setUserType(usertype);
        try{
            boolean res=UserDao.validateUser(u);
            if(res==false)
            {
                JOptionPane.showMessageDialog(null,"INVALID/USERID/USER_TYPE","ERROR",JOptionPane.ERROR_MESSAGE);
                txtUserId.setText("");
                jPassword.setText("");
                return ;
            }
            UserProfile.setUserId(userid);
            UserProfile.setUserType(usertype);
            if(jrManager.isSelected())
            {
                ManagerOptionsFrame mf=new ManagerOptionsFrame();
                mf.setVisible(true);
                this.dispose();
            }
            else 
            {
                ReceptionistOptionsFrame rf=new ReceptionistOptionsFrame();
                rf.setVisible(true);
                this.dispose();
            }
                
        }
        catch(SQLException sqle)
        {
            JOptionPane.showMessageDialog(null,"ERROR IN DB","ERROR",JOptionPane.ERROR_MESSAGE);
            sqle.printStackTrace();
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed

