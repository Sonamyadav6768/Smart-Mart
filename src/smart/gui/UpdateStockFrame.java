    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        
        if(jTableUpdateStock.getSelectedRow()==-1)
        {
            
        }
        if(!validateInput())
        {
            JOptionPane.showMessageDialog(null,"Please Fill all details");
            return ;
        }
        
        try{
            int len=jComboTax.getSelectedItem().toString().length();
            int tax=Integer.parseInt(jComboTax.getSelectedItem().toString().substring(0, len-1));
            ProductPojo p=new ProductPojo();
            p.setProductId(txtProductId.getText().trim());
            p.setProductName(txtProductName.getText().trim());
            p.setProductCompanyName(txtProductCompany.getText().trim());
            p.setProductPrice(Double.parseDouble(txtProductPrice.getText().trim()));
            p.setOurPrice(Double.parseDouble(txtOurPrice.getText().trim()));
            p.setQuantity(Integer.parseInt(txtQuantity.getText().trim()));
            p.setProductTax(tax);
            boolean res=ProductDao.updateProduct(p);
            if(res==true)
            {
                JOptionPane.showMessageDialog(null,"PRODUCT UPDATED SUCCESSFULLY");
                clearText();
                loadProductDetails();
                return ;
            }
            JOptionPane.showMessageDialog(null,"PRODUCT NOT UPDATED");
        }
        catch(SQLException sqle)
        {
             JOptionPane.showMessageDialog(null,"ERROR IN DB","ERROR",JOptionPane.ERROR_MESSAGE);
            sqle.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

