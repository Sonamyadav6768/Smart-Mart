    private void loadProductDetails() {
        try{
            
            List<ProductPojo> productList=ProductDao.getProductDetails();
            if(productList.isEmpty())
            {
                JOptionPane.showMessageDialog(null,"NO Product Found");
                btnUpdate.setEnabled(false);
                return ;
            }
            btnUpdate.setEnabled(true);
             tm=(DefaultTableModel)jTableUpdateStock.getModel();
             tm.setRowCount(0);
            Object [] arr=new Object[7];
            for(ProductPojo p:productList)
            {
                arr[0]=p.getProductId();
                arr[1]=p.getProductName();
                arr[2]=p.getProductCompanyName();
                arr[3]=p.getProductPrice();
                arr[4]=p.getOurPrice();
                arr[5]=p.getProductTax()+"%";
                arr[6]=p.getQuantity();
                tm.addRow(arr);
            }
        }
        catch(NumberFormatException nfe)
        {
            JOptionPane.showMessageDialog(null,"Please Enter Numeric Data","ERROR",JOptionPane.ERROR_MESSAGE);
            nfe.printStackTrace();
        }
         catch(SQLException sqle)
        {
            JOptionPane.showMessageDialog(null,"ERROR IN DB","ERROR",JOptionPane.ERROR_MESSAGE);
            sqle.printStackTrace();
        }
    }
    private boolean validateInput() {
        return !(txtProductName.getText().isEmpty() || txtProductCompany.getText().isEmpty() || 
                txtProductPrice.getText().isEmpty() || txtOurPrice.getText().isEmpty()  || txtQuantity.getText().isEmpty());
    }
    
