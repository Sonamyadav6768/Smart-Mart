    private void loadReceptionistId() {
        try
        {
           receptionistIdList=ReceptionistDao.getReceptionistId();
           if(receptionistIdList.isEmpty())
           {
               JOptionPane.showMessageDialog(null, "NO RECORD FOUND ");
               return;
           }
           btnRemove.setEnabled(true);
           for(String id:receptionistIdList)
               jComboReceptionistId.addItem(id);
        }
         catch(SQLException sqle)
        {
            JOptionPane.showMessageDialog(null,"ERROR IN DB","ERROR",JOptionPane.ERROR_MESSAGE);
            sqle.printStackTrace();
        }
    }
