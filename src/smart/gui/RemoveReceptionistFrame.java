    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        try{
            String id=jComboReceptionistId.getSelectedItem().toString();
            boolean res=ReceptionistDao.deleteReceptionist(id);
            if(res==true)
            {
                JOptionPane.showMessageDialog(null, "RECORD DELETED SUCCESSFULLY!!");
                jComboReceptionistId.removeItemAt(jComboReceptionistId.getSelectedIndex());
                if(jComboReceptionistId.getItemCount()==0)
                    btnRemove.setEnabled(false);
               return;
            }
           JOptionPane.showMessageDialog(null, "RECORD NOT DELETED");
        }
         catch(SQLException sqle)
        {
            JOptionPane.showMessageDialog(null,"ERROR IN DB","ERROR",JOptionPane.ERROR_MESSAGE);
            sqle.printStackTrace();
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

