class SplashThread extends Thread
{
    public void run()
    {
        int count=1;
        Random r=new Random();
        while(jProgressBar.getValue()<jProgressBar.getMaximum())
        {
            try{
                jProgressBar.setValue(count);
                Thread.sleep(1200);
                count+=r.nextInt(35);
            }
            catch(InterruptedException ex)
            {
                JOptionPane.showMessageDialog(null,"Splash Thread interrupted","ERROR",JOptionPane.ERROR_MESSAGE);
            }
        }
        SplashScreenFrame.this.dispose();
        LoginFrame login=new LoginFrame();
        login.setVisible(true);
    }
}

