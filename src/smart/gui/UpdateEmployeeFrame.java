    private void empIdLoad() {
        try{
            List<String> empIdList=EmployeesDao.fetchAllEmployeeId();
            for(String id:empIdList)
            {
                jComboEmployeeId.addItem(id);
            }
        }
        catch(SQLException sqle)
        {
            JOptionPane.showMessageDialog(null,"ERROR IN DB","ERROR",JOptionPane.ERROR_MESSAGE);
            sqle.printStackTrace();
            return ;
        }
    }

    private boolean validInput() {
        return !(txtEmployeeName.getText().isEmpty() || txtSalary.getText().isEmpty());
    }
