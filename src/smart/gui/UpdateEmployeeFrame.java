    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(validInput()==false)
        {
            JOptionPane.showMessageDialog(null, "Please Input all details ","ERROR",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            EmployeePojo emp=new EmployeePojo();
            emp.setEmpId((String)jComboEmployeeId.getSelectedItem());
            emp.setEmpName(txtEmployeeName.getText());
            emp.setJob((String)jComboJob.getSelectedItem());
            emp.setSalary(Double.parseDouble(txtSalary.getText()));
            boolean res=EmployeesDao.updateEmployee(emp);
            if(res==true)
            {
                JOptionPane.showMessageDialog(null,"Record Updated Successfully","MESSAGE",JOptionPane.INFORMATION_MESSAGE);
                txtEmployeeName.setText("");
                txtSalary.setText("");
                jComboJob.setSelectedIndex(0);
                jComboEmployeeId.setSelectedIndex(0);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Record NOT Updated Successfully","ERROR",JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(NumberFormatException nfe)
        {
            JOptionPane.showMessageDialog(null,"INPUT NUMERIC DATA","ERROR",JOptionPane.ERROR_MESSAGE);
            nfe.printStackTrace();
        }
        catch(SQLException sqle)
        {
            JOptionPane.showMessageDialog(null,"ERROR IN DB","ERROR",JOptionPane.ERROR_MESSAGE);
            sqle.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

